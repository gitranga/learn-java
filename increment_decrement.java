public class increment_decrement {
    public static void main(String args[]){
        int counter=20;
        counter ++; // counter = counter + 1;
        System.out.println(counter);
        counter --; // counter = counter - 1;
        System.out.println(counter);
        counter += 10; // counter = couunter + 10;
        System.out.println(counter);
        counter -= 10; // counter = couunter + 10;
        System.out.println(counter);
        counter *= 10; // counter = couunter + 10;
        System.out.println(counter);

    }
}
