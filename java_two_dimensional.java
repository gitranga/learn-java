public class java_two_dimensional {
    public static void main(String args[]){
        int [][] two_dim_ary = {{2,3,4,5},{0,9,8,7}};
        System.out.println(two_dim_ary[0][0]);
        System.out.println(two_dim_ary[0][1]);
        System.out.println(two_dim_ary[0][2]);
        System.out.println(two_dim_ary[1][0]);
        System.out.println(two_dim_ary[1][1]);
        System.out.println(two_dim_ary[1][2]);
    }

}
